CREATE TABLE LIBROS (
    ID INT AUTO_INCREMENT PRIMARY KEY,
    TITULO Varchar(120) NOT NULL,                 
    ISBN Varchar( 20) UNIQUE NOT NULL,           
    AÑO_PUBLICACION SMALLINT,
    EDITORIAL Varchar(100),
    PRECIO decimal(6,2) NOT NULL,
    MONEDA Varchar( 20),
    NUMERO_PAGINAS int, 
    STOCK Int NOT NULL,
    FECHA_INGRESO Date,
    DISPONIBILIDAD ENUM('Si','No')
    ); 



CREATE TABLE AUTORES(
    ID INT AUTO_INCREMENT PRIMARY KEY,
    NOMBRE_AUTOR VARCHAR( 100) NOT NULL,
    NACIONALIDAD VARCHAR ( 100),
    AÑO_DE_NACIMIENTO SMALLINT ,
    NUMERO_OBRAS INT,
    PREMIOS VARCHAR (255) NOT NULL 
);
    

CREATE TABLE LIBRO_AUTOR (
 ID INT AUTO_INCREMENT PRIMARY KEY,
 ID_LIBRO INT NOT NULL UNIQUE,
 ID_AUTOR INT NOT NULL UNIQUE,
 PORCENTAJE_COLABORACION DECIMAL(5,2),
 FOREIGN KEY (ID_LIBRO) REFERENCES LIBROS(ID),
 FOREIGN KEY (ID_AUTOR) REFERENCES AUTORES(ID)
        );




CREATE TABLE CATEGORIAS(
    ID INT AUTO_INCREMENT PRIMARY KEY,
NOMBRE_CATEGORIA VARCHAR( 100),
DESCRIPCION VARCHAR (255),
POPULARIDAD INT (11)
);




CREATE TABLE LIBRO_CATEGORIA(
    ID INT AUTO_INCREMENT PRIMARY KEY,
    ID_LIBRO INT,
    ID_CATEGORIA INT,
    NIVEL_IMPORTANCIA INT (11),
    FOREIGN KEY ( ID_LIBRO) REFERENCES LIBROS(ID),
    FOREIGN KEY ( ID_CATEGORIA) REFERENCES CATEGORIAS(ID)
    );




CREATE TABLE PERSONAS(
ID INT AUTO_INCREMENT PRIMARY KEY,
NOMBRE VARCHAR(100 ),
EMAIL VARCHAR(100),
TELEFONO Varchar (25),
FECHA_REGISTRO datetime,
CUOTA_MENSUAL decimal( 5,2),
ACTIVO enum ('Si','No')
);





CREATE TABLE PERSONAS_LIBROS(
    ID INT AUTO_INCREMENT PRIMARY KEY,
    ID_PERSONA INT,
    ID_LIBRO INT,
FECHA_PRESTAMO DATE,
FECHA_DEVOLUCION DATE NULL,
MORA INT,
    FOREIGN KEY ( ID_PERSONA) REFERENCES PERSONAS(ID),
    FOREIGN KEY ( ID_LIBRO) REFERENCES LIBROS(ID)
);



