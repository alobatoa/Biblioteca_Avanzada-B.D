CREATE VIEW vista_libros_autores AS
SELECT l.*,a.NOMBRE_AUTOR
FROM LIBROS l INNER JOIN LIBRO_AUTOR la 
ON la.ID_LIBRO = l.ID
INNER JOIN AUTORES a ON a.ID = la.ID_AUTOR;





CREATE VIEW vista_prestamos_activo3 AS
SELECT  P.NOMBRE,L.TITULO,P.ACTIVO
FROM PERSONAS P INNER JOIN personas_libros PT ON P.ID = PT.ID INNER JOIN LIBROS L ON PT.ID = L.ID
WHERE FECHA_DEVOLUCION = "0000-00-00";




